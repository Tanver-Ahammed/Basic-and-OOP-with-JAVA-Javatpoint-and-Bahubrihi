package basic;

public class CreateString {
    public static void main(String[] args) {

        char []ch = {'T','a','n','v','e','r'};
        // By new keyword
        String st = new String(ch);
        System.out.println(st);

        // By string literal
        String st1 = "Ahammed";
        System.out.println(st1);
    }
}
